    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Metodos me=new Metodos();
        
        DSalidas Sa = new DSalidas();
        
        Sa.setNombre(txtNombre.getText());
        Sa.setDescripcion(txtDescripcion.getText());
        Sa.setStock(Float.parseFloat(txtStockActual.getText()));
        Sa.setSalida(Float.parseFloat(txtCantidad.getText()));
        Date fechaSalida = FechaSalida.getDate();
        long d = fechaSalida.getTime();
        java.sql.Date soloFecha = new java.sql.Date(d);
        Sa.setFecha_Salida(soloFecha);
        Sa.setHora(hora().toString());
        Sa.setEstado("Salida");
        
        Sa.setCategoria(Categoria.getText());
        
        float StockActual = 0, Salida = 0, Resta=0;
        StockActual = (Float.parseFloat(txtStockActual.getText()));
        Salida = (Float.parseFloat(txtCantidad.getText()));
        Resta = StockActual - Salida;
        
        if(Resta>=0){
            me.ModificarStock(Resta, txtBuscar);
        me.GuardarSalidas(Sa);
        MostrarTablaSalidas(TablaSalidas);
        buscar();
        }else{
            JOptionPane.showMessageDialog(null, "Salida supera al stock actual: \nStock actual: "+StockActual);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

