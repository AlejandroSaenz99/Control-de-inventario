    public void buscar(){
        String BusquedaTicket = Metodos.Buscar_Id(txtBuscar.getText());
        if(BusquedaTicket.equals("Id no existe")){
            JOptionPane.showMessageDialog(null, "Producto no existe");
            txtBuscar.setText("");
        }else{
            DArticulos prov = new DArticulos();
            prov.setId(Integer.parseInt(this.txtBuscar.getText()));
            (new PuenteArticulo()).PuenteBuscarDatosArticulo2(prov);
            
            txtNombre.setText(prov.getNombre());
            txtDescripcion.setText(prov.getDescripcion());
            txtStockActual.setText(String.valueOf(prov.getStock()));
            Categoria.setText(prov.getCategoria());
            
            txtStockActual.setEnabled(false);
            btnGuardar.setEnabled(true);
        }
    }

