    public Marticulos(String tipoDeUsuario) {
        initComponents();
         btnReporte.setVisible(false);
        tipo = tipoDeUsuario;
        if (verificarReorden()) {
            Metodos me = new Metodos();
            TablaArticulo = me.MostrarTablaEnReorden(TablaArticulo);
            btnProductosReorden.setSelected(true);
            btnTodosLosProductos.setSelected(false);
             btnReporte.setVisible(true);
        } else {
            MostrarTablaArticulo(TablaArticulo);
            btnProductosReorden.setSelected(false);
            btnTodosLosProductos.setSelected(true);
             btnReporte.setVisible(false);
        }

        setLocationRelativeTo(null);
        setResizable(false);

        comboCategoria.addItem("");

        controlDeUsuarios();
    }

    public boolean verificarReorden() {
        boolean reorden;
        Metodos me = new Metodos();
        TablaArticulo = me.MostrarTablaEnReorden(TablaArticulo);
        if (TablaArticulo.getRowCount() > 0) {
            JOptionPane.showMessageDialog(null, TablaArticulo.getRowCount() + " articulo(s) se encuentra(n) en punto de reorden");
            reorden = true;
        } else {
            reorden = false;
        }
        return reorden;
    }

    public void MostrarTablaArticulo(JTable TablaArticulo) {
        (new PuenteArticulo()).PuenteMostrarTabla(TablaArticulo);
    }

    void MostrarTabla() {
        (new PuenteArticulo()).PuenteMostrarTablaTotalStaff(TablaArticulo, txtBuscar);
    }

    public void Buscar() {
        Metodos articulo = new Metodos();
        articulo.Buscar(TablaArticulo, txtBuscar.getText(), comboCategoria.getSelectedItem().toString());
    }

