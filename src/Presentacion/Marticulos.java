    private void TablaArticuloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaArticuloMouseClicked
        if (tipo.equals("Administrador") || tipo.equals("Almacenista")) {
            Modificar mo = new Modificar(tipo);
            if (evt.getButton() == 1) {
                int fila = TablaArticulo.getSelectedRow();
                mo.txtId.setText(TablaArticulo.getValueAt(fila, 0).toString());
                mo.txtId2.setText(TablaArticulo.getValueAt(fila, 0).toString());
                mo.txtNombre.setText(TablaArticulo.getValueAt(fila, 1).toString());
                mo.txtDescripcion.setText(TablaArticulo.getValueAt(fila, 2).toString());
                mo.txtCostoEntrada.setText(TablaArticulo.getValueAt(fila, 3).toString());

                mo.txtStock.setText(TablaArticulo.getValueAt(fila, 5).toString());

                //tomar fecha del valor de la tabla y ponerla en ell jdatechooser
                String fe = TablaArticulo.getValueAt(fila, 7).toString();
                DateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                try {
                    Date fecha = formato.parse(fe);
                    mo.FechaEntrada.setDate(fecha);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error" + e);
                }
                mo.Categoria.setSelectedItem(TablaArticulo.getValueAt(fila, 8).toString());
                mo.txtReorden.setText(TablaArticulo.getValueAt(fila, 9).toString());
                mo.btne.setEnabled(true);
                mo.btnm.setEnabled(true);
                String x = TablaArticulo.getValueAt(fila, 10).toString();

                try {
                    final Barcode barcode = BarcodeFactory.createCode128(mo.txtId2.getText());
                    barcode.setBarHeight(60);
                    barcode.setBarWidth(2);
                    mo.lbBarcode.setIcon(new ImageIcon(BarcodeImageHandler.getImage(barcode)));
                } catch (BarcodeException ex) {
                    Logger.getLogger(Modificar.class.getName()).log(Level.SEVERE, null, ex);
                } catch (OutputException ex) {
                    Logger.getLogger(Modificar.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (x.equals("Inactivo")) {
                    mo.btne.setEnabled(false);
                    mo.btnm.setEnabled(false);
                    mo.btnActivar.setVisible(true);
                }

                mo.setVisible(true);
                dispose();
            }
        }

    }//GEN-LAST:event_TablaArticuloMouseClicked

