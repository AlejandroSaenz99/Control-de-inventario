    private void btnaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaActionPerformed
        if (this.txtId.getText().isEmpty() || this.txtNombre.getText().isEmpty() || this.txtDescripcion.getText().isEmpty() || this.txtCostoEntrada.getText().isEmpty()
                || this.txtEntrada.getText().isEmpty() || this.FechaEntrada.getDate() == null
                || this.txtPuntoReorden.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "llenar todos los datos", "falta de datos", 1);
        }if(Categoria.getSelectedItem().toString().equals("Categoria")){JOptionPane.showMessageDialog(null, "Seleccione una categoria");}
        else {
            DArticulos prod = new DArticulos();
            prod.setId(Integer.parseInt(this.txtId.getText()));
            prod.setNombre(this.txtNombre.getText());
            prod.setDescripcion(this.txtDescripcion.getText());
            prod.setCosto_Entrada(Float.parseFloat(txtCostoEntrada.getText()));
            prod.setCosto_Promedio_U(Float.parseFloat(txtCostoEntrada.getText()));
            prod.setStock(Float.parseFloat(txtEntrada.getText()));
            prod.setEntrada(Float.parseFloat(txtEntrada.getText()));
            Date fechaEntrada = FechaEntrada.getDate();
            long d = fechaEntrada.getTime();
            java.sql.Date soloFecha = new java.sql.Date(d);
            prod.setFecha_Ultima_Entrada(soloFecha);
            prod.setCategoria(Categoria.getSelectedItem().toString());
            prod.setPunto_Reorden(Float.parseFloat(txtPuntoReorden.getText()));
            prod.setEstado("Activo");

            prod.setUnidad_Medida(txtMedida.getSelectedItem().toString());
            btna.setEnabled(false);
            ac = metodos.autocompletar(txtDescripcion);
            // Date fechaSalida=FechaSalida.getDate();
            //long d1=fechaSalida.getTime();
            //java.sql.Date soloFecha1=new java.sql.Date(d1);
            //prod.setFecha_Ultima_Salida(soloFecha1);

            (new PuenteArticulo()).PuenteGuardarArticulo(prod);
            limpiar();
            // MostrarTablaArticulo(TablaArticulo);

        }         // TODO add your handling code here:
    }//GEN-LAST:event_btnaActionPerformed

