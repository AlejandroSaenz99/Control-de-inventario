    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.txtId.getText().isEmpty() || this.txtCategoria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "llenar todos los datos", "falta de datos", 1);
        } else {
            DCategorias prod = new DCategorias();
            prod.setId(Integer.parseInt(txtId.getText()));
            prod.setNombre(txtCategoria.getText());
String Estado="Activo";
            (new PuenteArticulo()).PuenteGuardarCategoria(prod,Estado);
            txtCategoria.setText("");
            txtId.setText("" + BuscarId());
            MostrarTablaCategoria();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

