    private void TablaCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaCategoriaMouseClicked
        if (evt.getButton() == 1) {
            int fila = TablaCategoria.getSelectedRow();
            txtId.setText(TablaCategoria.getValueAt(fila, 0).toString());
            txtModificar.setText(TablaCategoria.getValueAt(fila, 1).toString());
            CAt= TablaCategoria.getValueAt(fila, 1).toString();
            Estado=TablaCategoria.getValueAt(fila, 2).toString();
            if(Estado.equals("Inactivo")){btnActivar.setVisible(true);}
            else if(Estado.equals("Activo")){btnActivar.setVisible(false);}
        }
    }//GEN-LAST:event_TablaCategoriaMouseClicked

